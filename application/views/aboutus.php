<p class="MsoNormal" style="mso-margin-top-alt:auto;mso-margin-bottom-alt:auto">
	<span lang="EN-US" style="font-size:11.0pt;mso-bidi-font-size:12.0pt;font-family:Arial;
mso-bidi-font-family:Arial">Deshal means Native. Started five years back Deshal now is a stylish, matured and contemporary brand providing clothes and fashion accessories to the youth of Bangladesh. Deshal conveys the native arts and crafts of Bangladesh through their designs. They use hand embroidery with creative metal, beads and glass beads materials on colourful local handlooms products. Their designs are inspired from local folk tales to various art patterns that are scattered all around us. <o:p></o:p></span></p>
<p class="MsoNormal" style="mso-margin-top-alt:auto;mso-margin-bottom-alt:auto">
	<span lang="EN-US" style="font-size:11.0pt;mso-bidi-font-size:12.0pt;font-family:Arial;
mso-bidi-font-family:Arial">Deshal started with only Fotua, a trendy yet famous type of shirt Bengali men wears. Now they make salwar kameez, sarees, skirts, tops, shawls, fotua, bapari shirt, sandals, bags, metal ornaments, household items (curtains, baskets, pillow covers etc) and kids wear. The 99-sqft small one room outlet has now emerged into a total 10000sqft area located in Banani, Shahbagh, Baily Road, Bashundhara City Shopping Mall and Chittagong.<o:p></o:p></span></p>
<p class="MsoNormal" style="mso-margin-top-alt:auto;mso-margin-bottom-alt:auto">
	<span lang="EN-US" style="font-size:11.0pt;mso-bidi-font-size:12.0pt;font-family:Arial;
mso-bidi-font-family:Arial">Bangladesh has been famous since ancient time for the outstanding weaving and creative materials such as Jamdani and Muslin. The pride however belongs to the handloom producers of the country. Conversely, in course of time due to various political, financial decisions and mass industrialization, the hand made products of Bangladesh is far from the glorious days. Like many other fashion houses Deshal too, patronizes local products and local craftsmanship. Deshal wants to uphold the native Bangladeshi materials, arts and crafts to the world, to popularize it in the global market. Thus the poor yet creative craftsman of Bangladesh will be able to produce the beautiful handmade products, which has been difficult lately due to the immense competition with machine made products. The love for native arts and crafts inspired Deshal to explore the world, for various other native arts and crafts, beside Bangladesh. As Deshal is expanding in a faster pace soon they will be able to incorporate the global arts and crafts in their product line. <o:p></o:p></span></p>
<p class="MsoNormal" style="mso-margin-top-alt:auto;mso-margin-bottom-alt:auto">
	<span lang="EN-US" style="font-size:11.0pt;mso-bidi-font-size:12.0pt;font-family:Arial;
mso-bidi-font-family:Arial">Deshal has earned immediate popularity for the affordable price and quality. The products are moderately priced hence the products are within the reach of mass people. The reason they cater to middle income people is they want the native art and heritage to reach to the mass people through their dresses.<o:p></o:p></span></p>
